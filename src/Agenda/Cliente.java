package Agenda;

public class Cliente {
	String horario;
	String nome;
	String email;
	String telefone;
	String servico;
	
	Cliente(String horario,String nome, String email, String telefone, String servico){
		this.horario = horario;
		this.nome = nome;
		this.email = email;
		this.telefone = telefone;
		this.servico = servico;
	}
}
